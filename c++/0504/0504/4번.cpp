#include <iostream>
using namespace std;

int foo(int n1, int n2 = 1, int n3 = 2, int n4 = 3);

int main()
{
    foo(10, 20, 30, 40);    // 10 : 20 : 30 : 40 
    foo(10, 20, 30);        // 10 : 20 : 30 : 3
    foo(10, 20);            // 10 : 20 : 2  : 3
    foo(10);                // 10 : 1 : 2 : 3

    return 0;
}

int foo(int n1, int n2, int n3, int n4)
{
    cout << n1 << " : " << n2 << " : " << n3 << " : " << n4 << endl;
    return n1 == n2 == n3 == n4;
}
